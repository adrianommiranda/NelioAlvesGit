package secao04;

public class Aula25 {

	public static void main(String[] args) {
		// Processamento de dados e casting
		int j, x = 5, y = 10;
		double k;

		j = x * y;
		k = j * 2;
		System.out.println(j);
		System.out.println(k);

		// fazendo casting
		double a = 10.00;
		int b;

		b = (int) a;
		System.out.println(b);
	}

}
